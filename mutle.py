    def __init__(self, **properties):
        # Set Form properties and Data Bindings.
        self.init_components(**properties)
        self.allwrds = self.readall().split("\n")
        self.solution = random.choice(self.readtgt().split("\n"))

        # Any code you write here will run when the form opens.
        keybrd = "—è—à–µ—Ä—Ç—ã—É–∏–æ–ø”±–∂–∞—Å–¥—Ñ–≥—á–π–∫–ª”ß—é—ç“•–∑—Ö—Ü–≤–±–Ω–º—â‚úÖ‚è™üîë"
        self.btn = {}
        gp = GridPanel()

        for i in keybrd:
            self.btn[i] = Button(text=i)
            self.btn[i].tag.name = i
            self.btn[i].set_event_handler("click", self.click)
            gp.add_component(self.btn[i], row="A", width_xs=1)
        self.add_component(gp)

    def click(self, **event_args):
        val = event_args["sender"].tag.name
        if val == "‚úÖ":
            self.text_area_1.text += self.heartify(self.text_area_1.text[-5:])
        elif val == "‚è™":
            self.text_area_1.text = self.text_area_1.text[:-1]
        elif val == "üîë":
            self.text_area_1.text += f"\n{self.solution}"
        else:
            self.text_area_1.text += event_args["sender"].tag.name

    def heartify(self, word):
        hearts = ""
        if word in self.allwrds:
            for i, letter in enumerate(word):
                if letter == self.solution[i]:
                    hearts += "üíö"
                elif letter in self.solution:
                    hearts += "üíõ"
                else:
                    hearts += "üñ§"
            return f"\n{hearts}\n"
        else:
            return ""

